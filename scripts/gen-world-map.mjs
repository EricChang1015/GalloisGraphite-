#!/usr/bin/env node
/**
 * Pre-compute SVG country paths and projected lon/lat → x/y pairs for the
 * supply map. Keeps d3-geo / world-atlas out of the client bundle.
 *
 * Run: npm run gen:world-map
 * Requires Node 18+ (plain ESM; no TypeScript flags).
 */

import { readFileSync, writeFileSync } from "node:fs";
import { geoNaturalEarth1, geoPath } from "d3-geo";
import { feature } from "topojson-client";

const root = new URL("..", import.meta.url);

const worldData = JSON.parse(
  readFileSync(new URL("node_modules/world-atlas/countries-110m.json", root), "utf8")
);

const { origin: ORIGIN, destinations: DESTINATIONS, latitudeGuides: LATITUDE_GUIDES } =
  JSON.parse(
    readFileSync(new URL("src/lib/maps/supply-map-points.json", root), "utf8")
  );

const VIEW_W = 1000;
const VIEW_H = 500;
const MADAGASCAR_ID = "450";

const projection = geoNaturalEarth1()
  .scale(190)
  .translate([VIEW_W / 2, VIEW_H / 2 + 10]);

const pathGen = geoPath(projection);

const topo = worldData;
const fc = feature(topo, topo.objects.countries);

function project(lon, lat) {
  return projection([lon, lat]) ?? [0, 0];
}

const countryPaths = fc.features
  .map((c) => {
    const d = pathGen(c);
    if (!d) return null;
    return { id: String(c.id ?? ""), d };
  })
  .filter(Boolean);

const projectedOrigin = project(ORIGIN.lon, ORIGIN.lat);

const projectedDestinations = Object.fromEntries(
  DESTINATIONS.map((d) => [d.id, project(d.lon, d.lat)])
);

const latitudeGuideLines = LATITUDE_GUIDES.map((line) => ({
  label: line.label,
  y: project(0, line.value)[1],
}));

const outPath = new URL("src/lib/maps/world.generated.ts", root);

const source = `// AUTO-GENERATED by scripts/gen-world-map.mjs — do not edit by hand.
// Re-run: npm run gen:world-map

export const VIEW_W = ${VIEW_W};
export const VIEW_H = ${VIEW_H};
export const VIEW_BOX = "0 0 ${VIEW_W} ${VIEW_H}";

/** ISO 3166-1 numeric code for Madagascar (highlighted on the supply map). */
export const MADAGASCAR_ID = "${MADAGASCAR_ID}";

export type CountryPath = {
  id: string;
  d: string;
};

export const COUNTRY_PATHS: CountryPath[] = ${JSON.stringify(countryPaths, null, 2)};

export const PROJECTED_ORIGIN: [number, number] = ${JSON.stringify(projectedOrigin)};

export const PROJECTED_DESTINATIONS: Record<string, [number, number]> = ${JSON.stringify(projectedDestinations, null, 2)};

export type LatitudeGuideLine = {
  label: string;
  y: number;
};

export const LATITUDE_GUIDE_LINES: LatitudeGuideLine[] = ${JSON.stringify(latitudeGuideLines, null, 2)};
`;

writeFileSync(outPath, source, "utf8");
console.log(`Wrote ${countryPaths.length} country paths → src/lib/maps/world.generated.ts`);
